    private void listarProdutosVendidos() {
        // tirei o: listarProdutos(String filtro)
        try {
            ProdutosDAO dao = new ProdutosDAO();
            List<ProdutosDTO> lista = dao.listarProdutosVendidos();

            DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
            listaProdutos.setRowSorter(new TableRowSorter(model));
            model.setNumRows(0);

            //Percorrer a lista e inserir na tabela
            for (ProdutosDTO L : lista) { //em cada volta do la√ßo for, o mesmo adiciona uma dado(film) dentro da variavel f
                Object[] obj = new Object[]{
                    L.getId(),
                    L.getNome(),
                    String.valueOf(L.getValor()),
                    L.getStatus()
                };
                //colocar os dados da variavel obj dentro da tabela
                model.addRow(obj);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
    }

